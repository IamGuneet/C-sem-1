#include <stdio.h>

void main()
{

    // c prog to display first 10 natural num

    for (int i = 1; i <= 10; i++)
    {
        printf("%i\n", i);
    }
    printf("\n");

    // c prog to display first 10 multiple of 2
    for (int i = 1; i <= 10; i++)
    {
        int multiple = 2 * i;
        printf("%i\n", multiple);
    }
}
